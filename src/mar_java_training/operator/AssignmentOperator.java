package mar_java_training.operator;

public class AssignmentOperator {
	public static void main(String[] args) {
		
		int i = 10;
		
//		i = i + 5;
		
		i+=15;  // i = i + 15;
		i-=15; // i = i - 15;
		i*=15; // i = i * 15;
		i/=15; // i = i / 15;
		i%=15; // i = i % 15;
		
		System.out.println(i);
		
		// Ternary operator
	}
}
